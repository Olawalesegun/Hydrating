public class Player {

    private String name;
    private int guessedNum;

    public void setName(String name) {
        this.name = name;
    }

    public void setGuessedNum(int num) {
        this.guessedNum = num;
    }

    public String getName() {
        return this.name;
    }

    public int getGuessedNum() {
        return this.guessedNum;
    }

}
